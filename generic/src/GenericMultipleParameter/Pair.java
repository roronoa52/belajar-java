package GenericMultipleParameter;

public class Pair <T, E>{
    public T getFirst() {
        return first;
    }

    public void setFirst(T first) {
        this.first = first;
    }

    public E getSecond() {
        return second;
    }

    public void setSecond(E second) {
        this.second = second;
    }

    private T first;
    private E second;

}
